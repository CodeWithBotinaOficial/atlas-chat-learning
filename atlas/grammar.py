    @staticmethod
    def capitalize_first(text: str) -> str:
        """Ensures the first letter of the response is uppercase."""
        if not text:
            return text
        # Find the first alphabetic character and capitalize it
        match = re.search(r'[a-zA-Z]', text)
        if match:
            idx = match.start()
            return text[:idx] + text[idx].upper() + text[idx+1:]
        return text

    @staticmethod
    def add_punctuation(text: str) -> str:
        """Adds a period at the end if the sentence doesn't end with . ! ? and is not empty."""
        if not text.strip():
            return text
        # Check if the last character is already a sentence-ending punctuation
        if text.strip()[-1] not in ['.', '!', '?']:
            return text.strip() + '.'
        return text.strip()

    @staticmethod
    def remove_excessive_repetition(text: str, max_repeats: int = 2) -> str:
        """
        If a word appears more than `max_repeats` times consecutively,
        reduce to `max_repeats` occurrences.
        Example: "hola hola hola mundo" -> "hola hola mundo"
        """
        if not text:
            return text
        words = text.split()
        if not words:
            return text

        cleaned_words = []
        i = 0
        while i < len(words):
            current_word = words[i]
            count = 0
            j = i
            while j < len(words) and words[j] == current_word:
                count += 1
                j += 1
            
            # Add the word up to max_repeats times
            for _ in range(min(count, max_repeats)):
                cleaned_words.append(current_word)
            
            i = j # Move to the next unique word sequence
        
        return ' '.join(cleaned_words)

    @staticmethod
    def filter_short_responses(text: str, min_words: int = 2,
                               fallback_message: str = "I'm not sure how to answer that properly.") -> str:
        """
        If the response has fewer than `min_words` words and is not a default message,
        replace with a fallback.
        """
        words = text.split()
        if len(words) < min_words and text.strip() != fallback_message.strip():
            # Check if the response is just punctuation or empty after stripping
            if not re.sub(r'[^\w\s]', '', text).strip(): # Remove punctuation and check if empty
                return fallback_message
            return fallback_message
        return text

    @staticmethod
    def _check_for_gibberish(text: str, stopword_ratio_threshold: float = 0.3) -> bool:
        """
        Checks if the response contains more than `stopword_ratio_threshold` of stopwords
        without content words, considering it gibberish.
        """
        words = re.findall(r'\b\w+\b', text.lower()) # Get all words, convert to lowercase
        if not words:
            return True # Empty or only punctuation is gibberish for this check

        stopword_count = sum(1 for word in words if word in GrammarHelper._SPANISH_STOPWORDS)
        
        # Calculate ratio of stopwords to total words
        if len(words) > 0:
            stopword_ratio = stopword_count / len(words)
        else:
            stopword_ratio = 0 # Should not happen if words is not empty

        # Consider it gibberish if stopword ratio is too high AND there are few content words
        # A simple heuristic: if more than 30% stopwords and less than 3 content words
        content_word_count = len(words) - stopword_count
        if stopword_ratio > stopword_ratio_threshold and content_word_count < 3:
            return True
        return False


    @staticmethod
    def apply_all(text: str, previous_user_message: str = None) -> str:
        """
        Chains all grammatical correction methods.
        """
        original_text = text.strip()
        if not original_text:
            return "I'm still learning to form proper sentences. Could you rephrase?"

        # 1. Remove excessive repetition
        processed_text = GrammarHelper.remove_excessive_repetition(original_text)
        
        # 2. Capitalize first letter
        processed_text = GrammarHelper.capitalize_first(processed_text)
        
        # 3. Add punctuation
        processed_text = GrammarHelper.add_punctuation(processed_text)

        # 4. Check for gibberish (high stopword ratio with few content words)
        if GrammarHelper._check_for_gibberish(processed_text):
            return "I'm still learning to form proper sentences. Could you rephrase?"

        # 5. Filter short responses (after other corrections, as they might shorten it)
        # Use a slightly different fallback for short responses vs gibberish
        final_response = GrammarHelper.filter_short_responses(
            processed_text,
            min_words=3, # Increased min_words slightly for final check
            fallback_message="I'm not sure how to answer that properly."
        )
        
        # Final check for empty or only punctuation after all processing
        if not re.sub(r'[^\w\s]', '', final_response).strip():
            return "I'm still learning to form proper sentences. Could you rephrase?"

        return final_response
